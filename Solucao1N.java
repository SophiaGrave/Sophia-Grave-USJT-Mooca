public class CalcularAreaPISolucao1N 
{
    public static void main(String[] args) 
    {
        // Valor do PI pedido no enunciado
        double PI = 3.14159;

        // O raio do circulo
        double raio = 2.0;

        // Area total do circulo
        double area = PI * raio * raio;

        // Resultado do valor
        System.out.println("A area do circulo com raio " + raio + " e: " + area);
    }
}
