public class DiasVividosSolucao1A 
{
    public static void main(String[] args) 
    {
        // Defina a idade da pessoa aqui
        int idadeEmAnos = 19; // Substitua por qualquer idade desejada
        
        // Calcule o número aproximado de dias vividos
        int diasVividos = idadeEmAnos * 365;
        
        // Exiba o resultado
        System.out.println("Você viveu aproximadamente " + diasVividos + " dias.");
    }
}
