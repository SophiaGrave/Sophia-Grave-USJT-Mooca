public class AreaDoTrianguloSolucao1Q
{
    public static void main(String[] agoraaprendi) 
    {
        // Escolha a base e a altura de um triangulo
        double base = 6.0;
        double altura = 3.0;

        // Calcule a area toda
        double area = (base * altura) / 2;

        // Resultado
        System.out.println("Base do triangulo: " + base);
        System.out.println("Altura do triangulo: " + altura);
        System.out.println("Area do triangulo: " + area);
    }
}
